package com.cg.operators;

public class Assignment {
	
	public static void main(String[] args) {
		//assignment operators 
		int a=40;
		a+=1;
		System.out.println(a);
		a-=1;
		System.out.println(a);
		a/=2;
		System.out.println(a);
		a*=2;
		System.out.println(a);
		a%=3;
		System.out.println(a);
		
	}

}
