package com.cg.operators;

public class Unary {

	public static void main(String[] args) {
		
		int a=8;
		System.out.println("initial value of a: "+a);
		++a;//pre-increment
		System.out.println(a);
		a++;//post-increment
		System.out.println(a);
		--a;//Pre-decrement
		System.out.println(a);
		a--;//post-decrement
		System.out.println(a);
	}

}
